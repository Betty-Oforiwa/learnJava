package src;

public class variables {
    public static void main (String[] args){
//        integer
        int x;
        int y;

        x = 4;
        y = 3;
        System.out.println(x + y);

//      floating numbers
        float a ;
        float b ;
        a = 4.5f;
        b = 54.5f;
        System.out.println(a + b);

//        int and float
        int iNumber = 5;
        float fNumber= 5.5f;
        System.out.println(iNumber);
        System.out.println(fNumber);


    }
}
